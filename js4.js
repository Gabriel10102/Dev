//Divisão por zero:
let resultado = 10 / 0;
console.log(resultado); // Resultado: Infinity

//Multiplicação:
//Multiplicação de dois números:
let multiplicacao1 = 5 * 3;
console.log(multiplicacao1); // Resultado: 15

//Multiplicação de números decimais:
let decimal1 = 2.5;
let decimal2 = 1.5;
let multiplicacao2 = decimal1 * decimal2;
console.log(multiplicacao2); // Resultado: 3.75

//Multiplicação por zero:
let resultado1 = 10 * 0;
console.log(resultado1); // Resultado: 0

//Combinação de operações:
//Expressão com soma e multiplicação:
let resultado2 = 2 + 3 * 4;
console.log(resultado2); // Resultado: 14 (multiplicação primeiro, depois soma)