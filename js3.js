//Subtração:
//Subtração de dois números:
let subtracao = 8 - 3;
console.log(subtracao); // Resultado: 5

//Subtração de números decimais:
let decimal1 = 5.7;
let decimal2 = 2.3;
let subtracao1 = decimal1 - decimal2;
console.log(subtracao1); // Resultado: 3.4

//Subtração variáveis:
let num1 = 10;
let num2 = 3;
let resultado = num1 - num2;
console.log(resultado); // Resultado: 7

//Divisão:
let divisao = 10 / 2;
console.log(divisao); // Resultado: 5

//Divisão de números decimais:
let decimal3 = 5.5;
let decimal4 = 2;
let divisao1 = decimal3 / decimal4;
console.log(divisao1); // Resultado: 2.75